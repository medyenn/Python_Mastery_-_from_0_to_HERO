def game():
    moves = []
    while True:
        move = input("What's your move : ").strip().lower()
        if move == "undo":
            undo = moves.pop()
            print(' -> '.join(moves), "| You've undone :", undo)
            continue
        if move == "restart":
            moves.clear()
            print(' -> '.join(moves), "| Let's start over :")
            continue
        if move == "stop":
            break
        moves.append(move)
        print(' -> '.join(moves))


main()
